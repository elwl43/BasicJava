package kr.or.ddit.study01;

public class VarExample {
	public static void main(String[] args) {
		System.out.println("실행문 끝에는 반드시 세미콜론(;)을 붙여야 합니다.");
		System.out.println(";은 실행문이 끝났음을 표시 합니다.");
		
		// 변수 : 값을 저장할 수 있는 공간
		int x;
		x=1;
		int y=2;
		int result = x+y;
		System.out.println(result);
	}
}
